import Asset from '../Asset/asset.js'

class Currency extends Asset {
  constructor({
    assetManagerId,
    assetId,
    assetClass='Currency',
    fungible,
    assetIssuerId,
    assetStatus='Active',
    countryId,
    venueId,
    currency,
    issueDate,
    maturityDate,
    description='',
    clientId,
    deliverable,
    minorUnitPlaces,
    comments,
    links,
    references,
    createdBy,
    updatedBy,
    createdTime,
    updatedTime,
    version
  }) {
    super({
      assetManagerId,
      assetId,
      assetClass,
      fungible,
      assetIssuerId,
      assetStatus,
      countryId,
      venueId,
      currency,
      issueDate,
      maturityDate,
      description,
      clientId,
      comments,
      links,
      references,
      createdBy,
      updatedBy,
      createdTime,
      updatedTime,
      version
    })
    this.deliverable = deliverable === false ? false : true // Default to true
    this.minorUnitPlaces = minorUnitPlaces
  }
}

export default Currency
